package kr.spring.club.vo;

public class ClubChatVO {
	private int club_chat_num;
	private int club_chat_leader;
	private int club_chat_participant;
	private String club_chat_title;
	
	public int getClub_chat_num() {
		return club_chat_num;
	}
	public void setClub_chat_num(int club_chat_num) {
		this.club_chat_num = club_chat_num;
	}
	public int getClub_chat_leader() {
		return club_chat_leader;
	}
	public void setClub_chat_leader(int club_chat_leader) {
		this.club_chat_leader = club_chat_leader;
	}
	public int getClub_chat_participant() {
		return club_chat_participant;
	}
	public void setClub_chat_participant(int club_chat_participant) {
		this.club_chat_participant = club_chat_participant;
	}
	public String getClub_chat_title() {
		return club_chat_title;
	}
	public void setClub_chat_title(String club_chat_title) {
		this.club_chat_title = club_chat_title;
	}
	
	
}
