package kr.spring.club.vo;

public class ClubFavVO {
	private int fav_num;
	private int club_num;
	private int mem_num;
	private int fav_code;
	
	public int getFav_num() {
		return fav_num;
	}
	public void setFav_num(int fav_num) {
		this.fav_num = fav_num;
	}
	public int getClub_num() {
		return club_num;
	}
	public void setClub_num(int club_num) {
		this.club_num = club_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getFav_code() {
		return fav_code;
	}
	public void setFav_code(int fav_code) {
		this.fav_code = fav_code;
	}

	
	
}
